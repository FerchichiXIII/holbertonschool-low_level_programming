#include "main.h"
/**
 * _putchar - writes the character c to stdout
 * @c: The character to print
 *
 * Return: On success 1.
 * On error, -1 is returned, and errno is set appropriately.
 */

void print_to_98(int n)
{

	if (n = 0)
	{
		while (n <= 98)
		{
			continue;
			_putchar(',');
			_putchar(' ');
		}
	}
	_putchar('\n');
	if (n = 111)
	{
		while (n >= 98)
		{
			continue;
			_putchar(',');
			_putchar(' ');
		}
		if (n = -10)
		{
			while (n <= 98)
			{
				continue;
				_putchar(',');
				_putchar(' ');
			}
		}
	}
}