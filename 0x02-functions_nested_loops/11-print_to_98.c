#include "main.h"
/**
 * void  prints all natural numbers from n to 98
 *
 * Return: On success 1.
 */

void print_to_98(int n)
{

	if (n = 0)
	{
		while (n <= 98)
		{
			continue;
			_putchar(',');
			_putchar(' ');
		}
		n++;
	}
	_putchar('\n');
	if (n = 111)
	{
		while (n >= 98)
		{
			continue;
			_putchar(n);
			_putchar(',');
			_putchar(' ');
		}
		n--;
	}
	if (n = -10)
	{
		while (n <= 98)
		{
			continue;
			_putchar(',');
			_putchar(' ');
		}
		n++;
	}
}
